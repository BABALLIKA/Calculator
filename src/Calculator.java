public class Calculator {

    public static int plus(int a, int b) {
        return a + b;
    }
    public static int minus(int a, int b) {
        return a - b;
    }
    public static int um(int a, int b) {
        return a * b;
    }
    public static int del(int a, int b) {
        return a / b;
    }
}

